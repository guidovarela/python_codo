""" Suma """
print(f'La suma entre {nro1} y {nro2} es {nro1 + nro2}')

""" RESTA """
print(f"La resta entre {nro1} y {nro2} es {nro1-nro2}")

""" Division (con decimales) """
print(f"La division entre {nro1} y {nro2} es {nro1/nro2}")

""" Division Entera """
print(f"La division entera entre {nro1} y {nro2} es {nro1//nro2}")

""" Multiplicacion """
print(f"El producto entre {nro1} y {nro2} es {nro1*nro2}")

""" Potencia """
print(f"La potencia entre {nro1} y {nro2} es {nro1**nro2}")

""" Modulo (es el resto de la division) """
print(f"El modulo entre {nro1} y {nro2} es {nro1%nro2}")

